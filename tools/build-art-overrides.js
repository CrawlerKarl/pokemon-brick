#!/usr/bin/env node
'use strict';

// Scans the AETHERFALL production-art FINAL folder and generates the tiny
// runtime override map the game loads (js/aetherfall-overrides.generated.js).
// Ground truth is the folder — an id appears in the map only when a
// validated final sprite actually exists, so the game can never reference
// missing art. Run after every generation batch:
//
//   npm run art-overrides
//
// Filename contract (from the production pipeline): af-<id>-<slug>.png
// The procedural renderer (js/aetherart.js) remains the always-present
// fallback for every id not in this map — the game works at ANY coverage
// level from 0% to 100%.

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const finalDir = path.join(root, 'art', 'aetherfall-production', 'sprites', 'final');
const outPath = path.join(root, 'js', 'aetherfall-overrides.generated.js');
const webBase = 'art/aetherfall-production/sprites/final/';

let files = [];
try {
  files = fs.readdirSync(finalDir).filter(f => /^af-\d+.*\.png$/i.test(f)).sort();
} catch (e) {
  console.log('build-art-overrides: no final/ directory yet — writing an empty map.');
}

const map = {};
const dupes = [];
for (const f of files) {
  const id = parseInt(f.match(/^af-(\d+)/i)[1], 10);
  if (map[id]) dupes.push(f);
  else map[id] = webBase + f;
}
if (dupes.length) {
  console.error('build-art-overrides: DUPLICATE ids skipped: ' + dupes.join(', '));
}

const body = [
  "'use strict';",
  '// GENERATED by tools/build-art-overrides.js — do not edit by hand.',
  '// Maps aetherfall ids to production-art sprites in ' + webBase,
  '// The procedural renderer covers every id absent from this map.',
  'const AETHERFALL_ART_OVERRIDES = ' + JSON.stringify(map, null, 2) + ';',
  '',
].join('\n');

fs.writeFileSync(outPath, body);
console.log('build-art-overrides: ' + Object.keys(map).length + ' override(s) → js/aetherfall-overrides.generated.js');
