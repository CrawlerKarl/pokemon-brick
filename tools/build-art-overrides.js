#!/usr/bin/env node
'use strict';

// Scans the AETHERFALL production-art FINAL folder and generates the tiny
// runtime override map the game loads (js/aetherfall-overrides.generated.js).
// Ground truth is the folder — an id appears in the map only when a
// validated final sprite actually exists, so the game can never reference
// missing art. Run after every generation batch:
//
//   npm run art-overrides
//
// Filename contract (from the production pipeline): af-<id>-<slug>.png
// The procedural renderer (js/aetherart.js) remains the always-present
// fallback for every id not in this map — the game works at ANY coverage
// level from 0% to 100%.

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const finalDir = path.join(root, 'art', 'aetherfall-production', 'sprites', 'final');
const previewDir = path.join(root, 'art', 'aetherfall-production', 'sprites', 'preview');
const outPath = path.join(root, 'js', 'aetherfall-overrides.generated.js');
const webBase = 'art/aetherfall-production/sprites/final/';

let files = [];
try {
  files = fs.readdirSync(finalDir).filter(f => /^af-\d+.*\.png$/i.test(f)).sort();
} catch (e) {
  console.log('build-art-overrides: no final/ directory yet — writing an empty map.');
}

// Two variants per id: the BASE sprite (af-<id>-<slug>.png) and the optional
// RADIANT form (af-<id>-<slug>-radiant.png — the pipeline's prismatic
// transform; the game's shiny-equivalent). Each goes in its own map — a
// radiant file must never shadow the base look.
const map = {};
const radiant = {};
const dupes = [];
for (const f of files) {
  const id = parseInt(f.match(/^af-(\d+)/i)[1], 10);
  const target = /-radiant\.png$/i.test(f) ? radiant : map;
  if (target[id]) dupes.push(f);
  else target[id] = webBase + f;
}
if (dupes.length) {
  console.error('build-art-overrides: DUPLICATE ids skipped: ' + dupes.join(', '));
}

// HIGH-RES VESSEL PREVIEWS (optional): the setup screens draw a chosen vessel
// far larger than its 128px gameplay sprite, where the upscale visibly softens.
// tools/build-aetherfall-previews.py re-keys the masters into 320px versions;
// absent entries simply fall back to the final, so a partial run is safe.
const preview = {};
const previewRadiant = {};
let previewFiles = [];
try {
  previewFiles = fs.readdirSync(previewDir).filter(f => /^af-\d+.*\.png$/i.test(f)).sort();
} catch (e) { /* no preview pass yet — the finals carry every draw */ }
for (const f of previewFiles) {
  const id = parseInt(f.match(/^af-(\d+)/i)[1], 10);
  const target = /-radiant\.png$/i.test(f) ? previewRadiant : preview;
  if (!target[id]) target[id] = 'art/aetherfall-production/sprites/preview/' + f;
}

const body = [
  "'use strict';",
  '// GENERATED by tools/build-art-overrides.js — do not edit by hand.',
  '// Maps aetherfall ids to production-art sprites in ' + webBase,
  '// The procedural renderer covers every id absent from this map.',
  'const AETHERFALL_ART_OVERRIDES = ' + JSON.stringify(map, null, 2) + ';',
  '// RADIANT forms (the shiny-equivalent): real prismatic art per id.',
  '// Ids absent here fall back to the base override + a hue-rotate filter.',
  'const AETHERFALL_ART_RADIANT = ' + JSON.stringify(radiant, null, 2) + ';',
  '// High-resolution previews for the large setup portraits (optional).',
  'const AETHERFALL_ART_PREVIEW = ' + JSON.stringify(preview, null, 2) + ';',
  'const AETHERFALL_ART_PREVIEW_RADIANT = ' + JSON.stringify(previewRadiant, null, 2) + ';',
  '',
].join('\n');

fs.writeFileSync(outPath, body);
console.log('build-art-overrides: ' + Object.keys(map).length + ' base + '
  + Object.keys(radiant).length + ' radiant + ' + Object.keys(preview).length + ' preview + '
  + Object.keys(previewRadiant).length + ' radiant-preview override(s) → js/aetherfall-overrides.generated.js');
